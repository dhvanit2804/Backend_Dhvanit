'Write a Python program to read a string, an integer, and a float from the keyboard and display them.'

string_value = input("Enter a string: ")
integer_value = int(input("Enter an integer: "))
float_value = float(input("Enter a float: "))

print("\n--- Displaying Entered Values ---")
print(f"String: {string_value}")
print(f"Integer : {integer_value}")
print(f"float : {float_value}")